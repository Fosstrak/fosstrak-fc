package org.accada.ale.server.readers;




/** The LRProperty is a name-value pair describing a attribute of a logical reader.
 * 
 * @author sawielan
 * @author haennimi
 */
public class LRProperty {

	/** The name of the LRProperty.*/
    protected String name;
    
    /** The value of the LRProperty.*/
    protected String value;

    /**Constructor of the LRProperty.*/
    public LRProperty(String name, String value){
    	this.name = name;
    	this.value = value;
    }
    
    /**
     * Gets the value of the name property.
     * 
     * @return
     *     possible object is
     *     {@link String }
     *     
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the value of the name property.
     * 
     * @param value
     *     allowed object is
     *     {@link String }
     *     
     */
    public void setName(String value) {
        this.name = value;
    }

    /**
     * Gets the value of the value property.
     * 
     * @return
     *     possible object is
     *     {@link String }
     *     
     */
    public String getValue() {
        return value;
    }

    /**
     * Sets the value of the value property.
     * 
     * @param value
     *     allowed object is
     *     {@link String }
     *     
     */
    public void setValue(String value) {
        this.value = value;
    }

}
